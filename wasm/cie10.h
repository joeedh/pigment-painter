float sample(float f, int idx) {
  f = std::min(std::max(f, 380.0f), 749.0f);
  f = (f - 380.0f) * 0.0027027f; // / 370
  f *= CIE10_STEPS;

  int i = (int)f;
  f -= i;

  if (i == CIE10_STEPS - 1) {
    return tables[idx][i];
  }

  float a = tables[idx][i];
  float b = tables[idx][i + 1];

  return a + (b - a) * f;
}

